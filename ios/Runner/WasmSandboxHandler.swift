private extension NSLock {
    @discardableResult
    func withLock<T>(_ body: () throws -> T) rethrows -> T {
        lock()
        defer { unlock() }
        return try body()
    }
}

// MARK: - Errors

private enum SandboxError: LocalizedError {
    case invalidURL
    case downloadFailed(Int)
    case sha256Mismatch(expected: String, got: String)

    var errorDescription: String? {
        switch self {
        case .invalidURL:
            return "Invalid Wasm download URL."
        case .downloadFailed(let code):
            return "Failed to download Alpine Wasm image (HTTP \(code))."
        case .sha256Mismatch(let expected, let got):
            return "Wasm image SHA256 mismatch. Expected \(expected), got \(got)."
        }
    }
}
